package com.carDealer.carDealer.cars.dto;

public class Make {
    private String id;
    private String name;

    public Make() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
